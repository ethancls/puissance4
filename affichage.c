void afficherPlateau(uint8_t plateau[LIGNES][COLONNES])
{
    printf("\n");
    for (uint8_t i = 0; i < LIGNES; i++)
    {
        printf("|");
        for (uint8_t j = 0; j < COLONNES; j++)
        {
            if (plateau[i][j] == JOUEUR1)
            {
                printf(" %s●%s |", ROUGE, DEFAUT);
            }
            else if (plateau[i][j] == JOUEUR2)
            {
                printf(" %s●%s |", JAUNE, DEFAUT);
            }
            else
            {
                printf("   |");
            }
        }
        printf("\n");

        for (uint8_t j = 0; j < COLONNES; j++)
        {
            printf("+---");
        }
        printf("+\n");
    }
    for (uint8_t j = 0; j < COLONNES; j++)
    {
        printf("  %d ", j + 1);
    }
    printf("\n\n");
}